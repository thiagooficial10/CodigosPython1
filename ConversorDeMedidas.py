#Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.

metros = float(input('Digite os metros: '))

print('A medida de ', metros, 'metros corresponde a: ', metros * 100, 'centimetros')
print('A medida de ', metros, 'metros corresponde a: ', metros * 1000, 'milimetros')